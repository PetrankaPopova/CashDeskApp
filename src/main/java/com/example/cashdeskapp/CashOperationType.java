package com.example.cashdeskapp;


/**
 * Enum representing types of cash operations.
 * <ul>
 *     <li>{@link #DEPOSIT}: Represents a deposit operation.</li>
 *     <li>{@link #WITHDRAWAL}: Represents a withdrawal operation.</li>
 * </ul>
 */
public enum CashOperationType {
    /**
     * Represents a deposit operation.
     */
    DEPOSIT,

    /**
     * Represents a withdrawal operation.
     */
    WITHDRAWAL
}